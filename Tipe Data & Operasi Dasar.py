# Tipe Data
#Integer (Bilangan Bulat)

x = 2
y = 4

print(x)
print(y)

#Float (Bilangan Pecahan)

a = 2.3
b = 9.2

print(a)
print(b)

#String (Text)

name = "Jacob"
greeting = "Hello"

print(name)
print(greeting)

#Boolean (True/False)

is_sunny = True
is_raining = False

print(is_sunny)
print(is_raining)


# Operasi Dasar
#Penjumlahan

result_penjumlahan = a + b
print(result_penjumlahan)

#Pengurangan

result_pengurangan = y - x
print(result_pengurangan)

#Perkalian

result_perkalian = y * x
print(result_perkalian)

#Pembagian

result_pembagian = y / x
print(result_pembagian)

#Pembagian Bulat = Hasilnya tanpa angka desimal

result_pembagianbulat = a // x
print(result_pembagianbulat)

#Modulo = untuk mendapatkan sisa pembagian

result_modulo = b % x
print(result_modulo)

#Pangkat

result_pangkat = y ** x
print(result_pangkat)






